const JWTStrategy   = passportJWT.Strategy;
const ExtractJWT = passportJWT.ExtractJwt;
const jwt = require('jsonwebtoken');

const payload ={
    id: 1,
    name: 'sayali',
    password: '12345'
};
const secret = 'your_jwt_secret';

const token = jwt.sign(payload, secret);
